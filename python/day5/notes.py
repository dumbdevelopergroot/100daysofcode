#For Loop with Lists
fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
  print(fruit)
  print(fruit + " Pie")
print(fruits)
for number in range(1,10):
    print(number)
for number in range(1,10,3):#here the last argument 3 specifies the step..
    print(number)#so output is 1,4,7(with 3 step)